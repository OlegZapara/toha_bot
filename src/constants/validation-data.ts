function escapeMarkdownV2(text: string) {
  const replacements: { [key: string]: string } = {
    _: "\\_",
    "*": "\\*",
    "[": "\\[",
    "]": "\\]",
    "(": "\\(",
    ")": "\\)",
    "~": "\\~",
    "`": "\\`",
    ">": "\\>",
    "#": "\\#",
    "+": "\\+",
    "-": "\\-",
    "=": "\\=",
    "|": "\\|",
    "{": "\\{",
    "}": "\\}",
    ".": "\\.",
    "!": "\\!",
  };

  return text
    .split("")
    .map((char) => replacements[char] || char)
    .join("");
}
